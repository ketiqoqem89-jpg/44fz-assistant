# 2. –ü–û–î–ì–û–¢–û–í–ö–ê –î–ê–ù–ù–´–• –í SIDEBAR (–¢—Ä–∏ –ø–æ–ª–æ—Å–∫–∏ –±—É–¥—É—Ç –≤—ã–∑—ã–≤–∞—Ç—å —ç—Ç–æ –º–µ–Ω—é)
with st.sidebar:
    st.title(f"üë§ {user_id}")
    if st.button("–í–´–ô–¢–ò"):
        for key in list(st.session_state.keys()): del st.session_state[key]
        st.rerun()
    
    st.markdown("---")
    st.subheader("–£–ø—Ä–∞–≤–ª–µ–Ω–∏–µ —á–∞—Ç–∞–º–∏")
    
    user_chats = db.get_user_chats(user_id)
    
    # –°–æ–∑–¥–∞–Ω–∏–µ —á–∞—Ç–∞
    new_name = st.text_input("–ù–∞–∑–≤–∞–Ω–∏–µ —á–∞—Ç–∞:", key="side_chat_input")
    if st.button("–°–û–ó–î–ê–¢–¨ –ß–ê–¢", key="side_create_btn"):
        if new_name:
            new_id = db.create_chat(user_id, new_name)
            if new_id:
                st.session_state.chat_id = new_id
                st.rerun()
    
    st.markdown("---")
    
    # –ü–ï–†–ï–ö–õ–Æ–ß–ï–ù–ò–ï –ß–ê–¢–û–í
    selected_chat_id = None
    if user_chats:
        chat_names = [c[1] for c in user_chats]
        chat_ids = [c[0] for c in user_chats]
        
        # –ï—Å–ª–∏ —á–∞—Ç –µ—â–µ –Ω–µ –≤—ã–±—Ä–∞–Ω, –±–µ—Ä–µ–º –ø–µ—Ä–≤—ã–π
        if "chat_id" not in st.session_state or st.session_state.chat_id not in chat_ids:
            st.session_state.chat_id = chat_ids[0]
            
        current_index = chat_ids.index(st.session_state.chat_id)
        pick = st.selectbox("–ü–µ—Ä–µ–∫–ª—é—á–∏—Ç—å —á–∞—Ç:", options=chat_names, index=current_index)
        
        # –û–±–Ω–æ–≤–ª—è–µ–º ID —á–∞—Ç–∞ –ø—Ä–∏ –≤—ã–±–æ—Ä–µ
